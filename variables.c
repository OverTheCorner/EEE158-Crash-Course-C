// Demonstrates variables and data types in C
#include <stdio.h>

int main() {
    int age = 20;
    float height = 1.75;
    char grade = 'A';
    printf("Age: %d\n", age);
    printf("Height: %.2f\n", height);
    printf("Grade: %c\n", grade);
    // Exercise: Declare your own variables and print them
    return 0;
}
